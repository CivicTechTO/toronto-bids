		<h2>About the site</h2>
		<p>Toronto Bids Archive is a project to preserve City of Toronto bid documents. It is run by volunteers, and <i>is in <u>no</u> way affiliated with the City of Toronto</i>.</p>
		<p>Toronto Bids Archive is in a pre-launch testing phase.</p>

		<h4>Why an archive of bid documents?</h4>
		<ol>
		<li>Currently, bid documents are not so easy to get access to thru the City
			<ul>
			<li>As a vendor, you can access them using the online portal, during the bid period only.</li>
			<li>Otherwise, you can request them via email, or file a FOI request.</li>
			</ul>
		</li>
		<li>Making these documents available is in the public interest
			<ul>
			<li>They can be useful to smaller vendors, and to the general public.</li>
			</ul>
		</li>
		<li>Making these documents available is useful to City staff
			<ul>
			<li>Learning from past procurements can save staff time and yield better outcomes.</li>
			</ul>
		</li>
		<li>The City has made a commitment to ‘Open By Default’
			<ul>
			<li>But technology or resource constraints can be barriers to this, in practice.</li>
			</ul>
		</li>		
		</ol>
		<p><i>Ideally, the City would archive and publish bid documents on its own.</i></p>
		
		<h4>What's included in the archive?</h4>
		<ul>
			<li>All bid documents published by the City of Toronto through its <a href="https://www.toronto.ca/business-economy/doing-business-with-the-city/searching-bidding-on-city-contracts/">online portal</a>. Closed tenders, and tenders by other entities like Toronto Police Service, TTC, Toronto Community Housing, and school boards operate separately and do not use the City of Toronto portal for posting tenders.</li>
			<li>Bid documents are not published on Toronto Bids Archive until the bidding period has closed.</li>
		</ul>

		<h4>Is is open source?</h4>
		<p>Yes, and the code's on GitHub: <a href="https://github.com/CivicTechTO/toronto-bids">https://github.com/CivicTechTO/toronto-bids</a>.</p>

		<h4>Contact</h4>
		<p>If you have questions about Toronto Bid Archive, contact gabe@pwd.ca.</p>
			
